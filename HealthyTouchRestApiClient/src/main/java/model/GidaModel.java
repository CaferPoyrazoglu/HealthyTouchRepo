package model;

public class GidaModel {
    
    private int gidano;
    private String gida;
    private double karbonhidrat;
    private double protein;
    private double yag;
    private String alerjen;

    public int getGidano() {
        return gidano;
    }

    public void setGidano(int gidano) {
        this.gidano = gidano;
    }

    public String getGida() {
        return gida;
    }

    public void setGida(String gida) {
        this.gida = gida;
    }

    public double getKarbonhidrat() {
        return karbonhidrat;
    }

    public void setKarbonhidrat(double karbonhidrat) {
        this.karbonhidrat = karbonhidrat;
    }

    public double getProtein() {
        return protein;
    }

    public void setProtein(double protein) {
        this.protein = protein;
    }

    public double getYag() {
        return yag;
    }

    public void setYag(double yag) {
        this.yag = yag;
    }

    public String getAlerjen() {
        return alerjen;
    }

    public void setAlerjen(String alerjen) {
        this.alerjen = alerjen;
    }
    
    

    
    
    
    
}

package pojo;

public class OrnekOgunModel {

    private int no;
    private String ad;
    private String ogun;
    private String malzemeler;

    public int getNo() {
        return no;
    }

    public void setNo(int no) {
        this.no = no;
    }

    public String getAd() {
        return ad;
    }

    public void setAd(String ad) {
        this.ad = ad;
    }

    public String getOgun() {
        return ogun;
    }

    public void setOgun(String ogun) {
        this.ogun = ogun;
    }

    public String getMalzemeler() {
        return malzemeler;
    }

    public void setMalzemeler(String malzemeler) {
        this.malzemeler = malzemeler;
    }

}

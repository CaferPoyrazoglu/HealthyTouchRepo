package pojo;

public class GidaBilgiModel {

    private double yag;
    private double protein;
    private double karbonhidrat;
    private String alerjen;
    private double toplam_miktar;

    /**
     * @return the yag
     */
    public double getYag() {
        return yag;
    }

    /**
     * @param yag the yag to set
     */
    public void setYag(double yag) {
        this.yag = yag;
    }

    /**
     * @return the protein
     */
    public double getProtein() {
        return protein;
    }

    /**
     * @param protein the protein to set
     */
    public void setProtein(double protein) {
        this.protein = protein;
    }

    /**
     * @return the karbonhidrat
     */
    public double getKarbonhidrat() {
        return karbonhidrat;
    }

    /**
     * @param karbonhidrat the karbonhidrat to set
     */
    public void setKarbonhidrat(double karbonhidrat) {
        this.karbonhidrat = karbonhidrat;
    }

    /**
     * @return the alerjen
     */
    public String getAlerjen() {
        return alerjen;
    }

    /**
     * @param alerjen the alerjen to set
     */
    public void setAlerjen(String alerjen) {
        this.alerjen = alerjen;
    }

    /**
     * @return the toplam_miktar
     */
    public double getToplam_miktar() {
        return toplam_miktar;
    }

    /**
     * @param toplam_miktar the toplam_miktar to set
     */
    public void setToplam_miktar(double toplam_miktar) {
        this.toplam_miktar = toplam_miktar;
    }
}
